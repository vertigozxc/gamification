export function getLocalizedQuestText(quest, field, language) {
  const normalizedLanguage = normalizeQuestLanguage(language);
  if (normalizedLanguage === "ru") {
    const localizedValue = RU_QUEST_TEXT[quest?.id]?.[field];
    if (typeof localizedValue === "string" && localizedValue.trim()) {
      return localizedValue;
    }
  }

  return String(quest?.[field] || "");
}